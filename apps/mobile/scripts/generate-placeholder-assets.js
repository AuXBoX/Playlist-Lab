#!/usr/bin/env node

/**
 * Generate placeholder assets for development
 * This creates simple colored squares as placeholders until real assets are ready
 * 
 * Usage: node scripts/generate-placeholder-assets.js
 */

const fs = require('fs');
const path = require('path');

const assetsDir = path.join(__dirname, '..', 'assets');

// Create assets directory if it doesn't exist
if (!fs.existsSync(assetsDir)) {
  fs.mkdirSync(assetsDir, { recursive: true });
  console.log('✓ Created assets directory');
}

// Create README in assets folder
const assetsReadme = `# Assets Directory

This directory contains app icons, splash screens, and other visual assets.

## Required Files

- \`icon.png\` - 1024x1024px universal app icon
- \`icon-ios.png\` - 1024x1024px iOS-specific icon
- \`adaptive-icon.png\` - 1024x1024px Android adaptive icon
- \`splash.png\` - 1242x2436px splash screen
- \`favicon.png\` - 48x48px web favicon
- \`notification-icon.png\` - 96x96px Android notification icon

## Placeholder Assets

The current assets are placeholders generated by the build script.
Replace these with your actual branded assets before production release.

See \`ASSETS_GUIDE.md\` in the parent directory for detailed specifications.

## Generating Real Assets

1. Design your icons and splash screens according to the specifications
2. Export at the required dimensions
3. Replace the placeholder files in this directory
4. Rebuild the app: \`eas build --platform all\`

## Tools

- [App Icon Generator](https://appicon.co/)
- [Expo Asset Generator](https://github.com/expo/expo-cli)
- [Android Asset Studio](https://romannurik.github.io/AndroidAssetStudio/)
`;

fs.writeFileSync(path.join(assetsDir, 'README.md'), assetsReadme);
console.log('✓ Created assets/README.md');

// Create placeholder asset descriptions
const placeholders = [
  {
    name: 'icon.png',
    size: '1024x1024',
    description: 'Universal app icon',
  },
  {
    name: 'icon-ios.png',
    size: '1024x1024',
    description: 'iOS-specific app icon',
  },
  {
    name: 'adaptive-icon.png',
    size: '1024x1024',
    description: 'Android adaptive icon foreground',
  },
  {
    name: 'splash.png',
    size: '1242x2436',
    description: 'Splash screen',
  },
  {
    name: 'favicon.png',
    size: '48x48',
    description: 'Web favicon',
  },
  {
    name: 'notification-icon.png',
    size: '96x96',
    description: 'Android notification icon',
  },
];

// Create placeholder info file
const placeholderInfo = `# Placeholder Assets

These are placeholder assets generated for development purposes.

## Files

${placeholders.map(p => `- **${p.name}** (${p.size}) - ${p.description}`).join('\n')}

## Next Steps

1. Replace these placeholders with your actual branded assets
2. Follow the specifications in ASSETS_GUIDE.md
3. Ensure all files are optimized (< 500KB for icons, < 1MB for splash)
4. Test on both iOS and Android devices
5. Rebuild the app after replacing assets

## Important Notes

- These placeholders are simple colored squares
- They are NOT suitable for production use
- Replace them before submitting to app stores
- Keep source files (SVG, Figma, etc.) for future updates

## Creating Real Assets

See the ASSETS_GUIDE.md file for detailed instructions on:
- Design guidelines
- Required dimensions
- Platform-specific considerations
- Tools and resources
- Testing procedures

Generated on: ${new Date().toISOString()}
`;

fs.writeFileSync(path.join(assetsDir, 'PLACEHOLDER_INFO.md'), placeholderInfo);
console.log('✓ Created assets/PLACEHOLDER_INFO.md');

console.log('\n📝 Asset placeholders documented');
console.log('\n⚠️  IMPORTANT: Replace placeholder assets with real branded assets before production!');
console.log('\nNext steps:');
console.log('1. Design your app icons and splash screens');
console.log('2. Export at required dimensions (see ASSETS_GUIDE.md)');
console.log('3. Place files in apps/mobile/assets/');
console.log('4. Run: eas build --platform all');
console.log('\nFor detailed specifications, see: apps/mobile/ASSETS_GUIDE.md\n');
